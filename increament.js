//increment & decrement in javascript
//post-increament 
// it outputs the number first then increases 
console.log("post_increament" );
var a=0;
console.log(a++);
// now if we ask for an output it gives us the increased number
console.log(a);

//pre increament 
console.log("pre_increament");
// it increases the number before giving us an output
var b= 5;
console.log(++b);

//post-decreament 
//outputs the current element before decreasing it 
console.log("post_decreament");
var a=5;
console.log(a--);
// now if we ask for an output it gives us the decreased number
console.log(a);

//pre decreament 
console.log("pre-decreament");
//outputs the decreased value instantly 
var a=27;
console.log(--a);