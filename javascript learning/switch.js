//switch statement in javscript 

var gender= "female";

switch (gender) {
    case "M":
        console.log ("male")
        break;
    case "male":
        console.log ("male")
        break;
    case "F":
        console.log ("female")
        break;
    case "female":
        console.log ("female")
        break;        
    default:
        console.log("unknown")
        break;
}